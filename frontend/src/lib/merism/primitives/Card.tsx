import { forwardRef, type HTMLAttributes } from "react";

import { cn } from "../utils/cn";

export const Card = forwardRef<HTMLDivElement, HTMLAttributes<HTMLDivElement>>(
  ({ className, ...props }, ref) => (
    <div
      ref={ref}
      className={cn(
        // No solid border — Outset-style "invisible structure".
        // Hairline ring provides a 1px edge at 6% opacity,
        // shadow-merism-card does the actual elevation.
        "rounded-merism-lg bg-merism-surface text-merism-text " +
          "ring-1 ring-[color:var(--merism-hairline)] shadow-merism-card " +
          "transition-shadow duration-[var(--merism-duration-base)] ease-[var(--merism-ease)]",
        className,
      )}
      {...props}
    />
  ),
);
Card.displayName = "Card";

